document.querySelector('.p').classList.add('text_horse')
document.querySelector('.h2').classList.add('text_horse')


function visible() {
    document.querySelector('.p').classList.remove('text_horse')
    document.querySelector('.h2').classList.remove('text_horse')
}

function novisible() {
    document.querySelector('.p').classList.add('text_horse')
    document.querySelector('.h2').classList.add('text_horse')
}